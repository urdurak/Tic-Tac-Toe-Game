
class Color:
    def __new__(cls, color_name: str):
        if color_name.lower() in cls.COLOR_DICT:
            hex_code = cls.COLOR_DICT[color_name.lower()]
        else:
            raise ValueError(f"Unsupported color: {color_name}")

        instance = super(Color, cls).__new__(cls)
        instance.name = color_name.lower()
        instance.hex = hex_code
        instance.rgb = cls.hex_to_rgb(hex_code)
        instance.r, instance.g, instance.b = instance.rgb
        return instance.rgb

    @classmethod
    def hex_to_rgb(cls, hex_code: str) -> tuple:
        hex_code = hex_code.lstrip('#')
        return tuple(int(hex_code[i:i + 2], 16) for i in (0, 2, 4))

